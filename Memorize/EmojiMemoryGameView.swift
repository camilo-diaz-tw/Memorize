import SwiftUI

// This is my View
struct EmojiMemoryGameView: View {
  // @ObservedObject property wrapper to indicate that the model can chage and the ui needs to update acordingly.
  @ObservedObject var game: EmojiMemoryGame
  
  var body: some View {
    ScrollView {
      LazyVGrid(columns: [GridItem(.adaptive(minimum: 100))]) {
        ForEach(game.cards) { card in
          CardView(card: card)
            .aspectRatio(2/3, contentMode: .fit)
            .onTapGesture {
              game.choose(card)
            }
        }
      }
    }
    .foregroundColor(.red)
    .padding(.horizontal)
  }
}

struct CardView: View {
  let card: EmojiMemoryGame.Card
  
  var body: some View {
    GeometryReader { geometry in
      ZStack {
        let shape = RoundedRectangle(cornerRadius: DrawingConstants.cornerRadius)
        if card.isFaceUp {
          shape.fill().foregroundColor(.white)
          shape.strokeBorder(lineWidth: DrawingConstants.strokeWdith)
          Text(card.content).font(font(in: geometry.size))
        } else if card.isMatched {
          shape.opacity(0)
        } else {
          shape.fill()
        }
      }
    }
  }
  
  private func font(in size: CGSize) -> Font {
    Font.system(size: min(size.height, size.width) * DrawingConstants.fontScale)
  }
  
  private struct DrawingConstants {
    static let cornerRadius: CGFloat = 20
    static let strokeWdith: CGFloat = 3
    static let fontScale: CGFloat = 0.8
  }
}

